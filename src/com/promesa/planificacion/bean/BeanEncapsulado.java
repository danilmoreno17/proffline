	public String getStrCodVen() {
		return strCodVen;
	}
	public void setStrCodVen(String strCodVen) {
		this.strCodVen = strCodVen;
	}
	
	public String getStrNomVen() {
		return strNomVen;
	}
	public void setStrNomVen(String strNomVen) {
		this.strNomVen = strNomVen;
	}
	
