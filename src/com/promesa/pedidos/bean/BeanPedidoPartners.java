package com.promesa.pedidos.bean;

import java.util.HashMap;

public class BeanPedidoPartners {

	HashMap<String, String> hm = new HashMap<String, String>();

	private String id_header = null;
	private String txtSincronizado = null;
	private String txtKey = null;
	private String txtValue = null;
	private String id = null;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public HashMap<String, String> getHm() {
		return hm;
	}

	public void setHm(HashMap<String, String> hm) {
		this.hm = hm;
	}

	public String getId_header() {
		return id_header;
	}

	public void setId_header(String id_header) {
		this.id_header = id_header;
	}

	public String getTxtSincronizado() {
		return txtSincronizado;
	}

	public void setTxtSincronizado(String txtSincronizado) {
		this.txtSincronizado = txtSincronizado;
	}

	public String getTxtKey() {
		return txtKey;
	}

	public void setTxtKey(String txtKey) {
		this.txtKey = txtKey;
	}

	public String getTxtValue() {
		return txtValue;
	}

	public void setTxtValue(String txtValue) {
		this.txtValue = txtValue;
	}

	@Override
	public String toString() {
		return "BeanPedidoPartners [hm=" + hm + "]";
	}
}