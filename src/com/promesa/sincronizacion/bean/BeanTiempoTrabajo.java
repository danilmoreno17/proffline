package com.promesa.sincronizacion.bean;

import java.util.Date;
import java.util.Timer;

import javax.swing.JPopupMenu;

public class BeanTiempoTrabajo {	
	private Timer temporizador;
	private Date date;
	private int frecuenacia;
	private String tarea;
	private String opciones;
	private String codigoUsuario;
	private JPopupMenu popupOpciones;
	private boolean activaInicio;

	public BeanTiempoTrabajo(Timer temporizador,Date date,int frecuenacia,String tarea,String opciones,String codigoUsuario,JPopupMenu popupOpciones,boolean activaInicio){
		this.temporizador=temporizador;
		this.date=date;
		this.frecuenacia=frecuenacia;
		this.tarea=tarea;
		this.opciones=opciones;		
		this.codigoUsuario=codigoUsuario;
		this.popupOpciones=popupOpciones;
		this.activaInicio=activaInicio;
	}
	
	public BeanTiempoTrabajo(){ }
	
	public Timer getTemporizador() {
		return temporizador;
	}
	public void setTemporizador(Timer temporizador) {
		this.temporizador = temporizador;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getFrecuenacia() {
		return frecuenacia;
	}
	public void setFrecuenacia(int frecuenacia) {
		this.frecuenacia = frecuenacia;
	}
	public String getTarea() {
		return tarea;
	}
	public void setTarea(String tarea) {
		this.tarea = tarea;
	}
	public String getOpciones() {
		return opciones;
	}
	public void setOpciones(String opciones) {
		this.opciones = opciones;
	}
	public String getCodigoUsuario() {
		return codigoUsuario;
	}
	public void setCodigoUsuario(String codigoUsuario) {
		this.codigoUsuario = codigoUsuario;
	}

	public JPopupMenu getPopupOpciones() {
		return popupOpciones;
	}

	public void setPopupOpciones(JPopupMenu popupOpciones) {
		this.popupOpciones = popupOpciones;
	}
	public boolean isActivaInicio() {
		return activaInicio;
	}

	public void setActivaInicio(boolean activaInicio) {
		this.activaInicio = activaInicio;
	}
}