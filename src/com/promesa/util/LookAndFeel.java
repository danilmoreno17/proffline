package com.promesa.util;

import javax.swing.ImageIcon;
import javax.swing.JFrame;

public class LookAndFeel {
	public static void IconoVentana(JFrame frame) {
		frame.setIconImage(new ImageIcon("imagenes/icono.jpg").getImage());
		frame.getContentPane().setBackground(new java.awt.Color(235, 239, 242));
	}
}